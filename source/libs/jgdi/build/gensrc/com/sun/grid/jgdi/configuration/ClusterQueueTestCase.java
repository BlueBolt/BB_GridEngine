   public void testClusterQueueAddGetDelete() throws Exception {
   
      Object[] testValues = TestValueFactory.getTestValues(ClusterQueue.class);
      assertTrue("We have no test values for ClusterQueue", (testValues.length > 0));

      JGDI jgdi = createJGDI();
      try {
         List<Util.Difference> differences = new LinkedList<Util.Difference>();
         for (int i = 0; i < testValues.length; i++) {
            ClusterQueue testObj = (ClusterQueue)testValues[i];
            logger.info("adding testObj " + testObj);
            jgdi.addClusterQueue(testObj);
            try {
              logger.info("fetching and diffing testObj " + testObj);
              ClusterQueue retObj = jgdi.getClusterQueue(testObj.getName());
              
              Util.getDifferences(testObj, retObj, differences);
              if (!differences.isEmpty()) {
                 logger.warning("org ClusterQueue is not equal to stored ClusterQueue ------------------------"); 
                 for (Util.Difference diff: differences) {
                    logger.warning(diff.toString());
                 }
              }
              //assertTrue("retobj is not equals to the testobj", differences.isEmpty());
              differences.clear();
              
            } finally {
              jgdi.deleteClusterQueue(testObj);
              
              ClusterQueue retObj = jgdi.getClusterQueue(testObj.getName());
              
              logger.info("deleting testObj " + testObj);
              assertNull(testObj + " has not been deleted", retObj);
            }
         }
      } finally {
        jgdi.close();
      }
   }


   public void testUserSetEvents() throws Exception {
         
      Object [] testValues = TestValueFactory.getTestValues(UserSet.class); 

      assertTrue("No test values for UserSet", testValues.length > 0 );
      
      UserSet testObj = (UserSet)testValues[0];


      WaitForEventThread waitThread = new WaitForEventThread(testObj);

      int flushInterval = 1; // in seconds
      long timeout = flushInterval * 1000 * 3;
      Map<EventTypeEnum,Integer> map = new HashMap<EventTypeEnum,Integer>();
      try {

          evc.addEventListener(waitThread);

          map.put(EventTypeEnum.UserSetAdd, flushInterval);
          map.put(EventTypeEnum.UserSetDel, flushInterval);
         
         
          map.put(EventTypeEnum.UserSetMod, flushInterval);
         
         evc.subscribe(map.keySet());            
         evc.setFlush(map);

         evc.commit();
         
         waitThread.start();

         Thread.yield();

         jgdi.addUserSet(testObj);
         logger.fine(testObj + " added");
         assertTrue("UserSet: Did not get the add event", waitThread.waitForAddEvent(timeout));

         
         // @jgdi.todo modify the test object

         jgdi.updateUserSet(testObj);
         logger.fine(testObj + " modified");
         assertTrue("UserSet: Did not get the mod event", waitThread.waitForModEvent(timeout));
         

         jgdi.deleteUserSet(testObj);
         logger.fine(testObj + " deleted");
         testObj = null;

         assertTrue("UserSet: Did not get the del event", waitThread.waitForDelEvent(timeout));

      } finally {
         waitThread.interrupt();
         evc.unsubscribe(map.keySet());
         evc.commit();
         
         if (testObj != null) {
            logger.fine(testObj + " deleted again");
            try {
               jgdi.deleteUserSet(testObj);
            } catch(Exception e) {
               // ignore
            }
         }
      }
   }


